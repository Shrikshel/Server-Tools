# Log functions
log_info() {
  printf "\033[1;34m%10s  %s\033[0m\n" "[INFO]" "$*"
}

log_warn() {
  printf "\033[1;33m%10s  %s\033[0m\n" "[WARN]" "$*"
}

log_error() {
  printf "\033[1;31m%10s  %s\033[0m\n" "[ERROR]" "$*"
}

log_success() {
  printf "\033[1;32m%10s  %s\033[0m\n" "[SUCCESS]" "$*"
}

log_debug() {
  printf "\033[1;90m%10s  %s\033[0m\n" "[DEBUG]" "$*"
}

log_fatal() {
  printf "\033[1;41m%10s  %s\033[0m\n" "[FATAL]" "$*"
}

log_orange() {
  printf "\033[1;38;5;208m%10s  %s\033[0m\n" "$*"
}

# Boxed/pretty log functions
log_header() {
  echo -e "\n\033[1;34m╭────────────────────────────────────────────────────────────────────────────────────────╮"
  echo -e "│  🚀 Running: $1 "
  echo -e "╰────────────────────────────────────────────────────────────────────────────────────────╯\033[0m"
}

log_footer() {
  echo -e "\033[1;32m╭────────────────────────────────────────────────────────────────────────────────────────╮"
  echo -e "│  ✅   $1 "
  echo -e "╰────────────────────────────────────────────────────────────────────────────────────────╯\n\033[0m"
}

log_warning_box() {
  echo -e "\033[1;33m╭────────────────────────────────────────────────────────────────────────────────────────╮"
  echo -e "│  ⚠️   $1 "
  echo -e "╰────────────────────────────────────────────────────────────────────────────────────────╯\n\033[0m"
}

log_error_box() {
  echo -e "\033[1;31m╔══════════════════════════════════════════════════════════════╗"
  echo -e "║  ❌  $1"
  echo -e "╚══════════════════════════════════════════════════════════════╝\033[0m"
}

